size = 10

for i in range (size):
    print("*", end="")

# This code prints a horizontal line of asterisks (*) with a length of 10 characters.
# The outer loop iterates 10 times, and in each iteration, it prints a single asterisk without moving to a new line.
# The end="" argument in the print function prevents the cursor from moving to the next line after printing each asterisk.
# As a result, all 10 asterisks are printed on the same line.   
# After the loop completes, the cursor remains on the same line.    